<br>
ร้านส้มตำที่มีชื่อเสียงโด่งดังจากเมนูตำถาด การันตีด้วยลูกค้าที่เข้าคิวยาว จึงใช้ชื่อว่าตำถาดบันลือโลก และสิ่งที่ดึงดูดมัดใจลูกค้าถึงกับต้องเข้าคิวยาวนั่นคือเมนู ตำถาดไทย ซึ่งได้รวบรวมวัตถุดิบหลากหลายอย่างอยู่ในถาดเดียว ได้แก่ ส้มตำไทย, ปลาดุกฟู, ไก่ทอด, ผักรวมทอด, หมูยอ, หมี่ลวก, ขนมจีน, ผักกาดขาว, แคบหมู, ไข่ต้ม นอกจากนี้ลูกค้ายังสามารถเลือกเป็นตำถาดป่า, ตำถาดลาวได้อีกด้วย และทีเด็ดคือ เราจะชูโรงด้วยน้ำปรุงรสสูตรเฉพาะ เป็นน้ำปรุงที่เคี่ยวประมาณ 6-8 ชม. จะทำให้รสชาติส้มตำเด่นขึ้นมาแปลกกว่าที่อื่น ท้าให้ลิ้มลอง<br>
<br>
ตั้งอยู่ที่ ถนนบรมราชชนนี ร้านอยู่ภายในตลาดปิ่นเงิน พลาซ่า ด้านในสุด ตรงข้ามเซ็นทรัล พลาซา ปิ่นเกล้า<br>
